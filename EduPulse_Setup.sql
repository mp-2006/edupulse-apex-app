-- 4. THE MAGIC: PRE-CONFIGURED VIEWS FOR CHARTS
-- Charts-la "SQL Query" pattiya indha view names-a mattum use pannunga.
--------------------------------------------------------

-- For Bar Chart (Student Performance)
CREATE OR REPLACE VIEW chart_performance AS
SELECT full_name AS label, score AS value FROM ep_students s 
JOIN ep_assessments a ON s.student_id = a.student_id;

-- For Donut/Pie Chart (Attendance)
CREATE OR REPLACE VIEW chart_attendance AS
SELECT status AS label, COUNT(*) AS value FROM ep_attendance GROUP BY status;

-- For Summary Report
CREATE OR REPLACE VIEW chart_summary AS
SELECT b.batch_name, COUNT(s.student_id) as total_students 
FROM ep_batches b LEFT JOIN ep_students s ON b.batch_id = s.batch_id 
GROUP BY b.batch_name;