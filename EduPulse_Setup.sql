-- 1. DROP EXISTING OBJECTS (Fresh Start)
BEGIN
    FOR t IN (SELECT table_name FROM user_tables WHERE table_name IN ('EP_ASSESSMENTS', 'EP_ATTENDANCE', 'EP_STUDENTS', 'EP_BATCHES')) LOOP
        EXECUTE IMMEDIATE 'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS';
    END LOOP;
END;
/

-- 2. CREATE DATABASE STRUCTURE
CREATE TABLE ep_batches (
    batch_id    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    batch_name  VARCHAR2(100) NOT NULL,
    stream      VARCHAR2(100)
);

CREATE TABLE ep_students (
    student_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name    VARCHAR2(100) NOT NULL,
    batch_id     NUMBER REFERENCES ep_batches(batch_id)
);

CREATE TABLE ep_attendance (
    att_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id   NUMBER REFERENCES ep_students(student_id),
    status       VARCHAR2(10) CHECK (status IN ('Present', 'Absent')),
    log_date     DATE DEFAULT SYSDATE
);

CREATE TABLE ep_assessments (
    eval_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id   NUMBER REFERENCES ep_students(student_id),
    score        NUMBER CHECK (score BETWEEN 0 AND 100),
    subject      VARCHAR2(50)
);

